							<h3>Bombones de Frutas Fleibor</h3> 
							<br>
							<p><strong>Ingredientes</strong></p>
							<p>200 g de puré de manzanas</p>
							<p>250 g de azúcar común</p>
							<p>2 cucharadas de pectina en polvo</p>
							<p>70 g de glucosa</p>
							<p>1 cucharada de esencia de frutilla Fleibor</p>
							<p>Colorante en pasta Fleibor a gusto</p>
							<p>Azúcar común para rebozar C/N</p>
							<br>
							<p><strong>Preparación</strong></p>
<p>Forrar el interior de un molde rectangular chico, o un hermético con una lamina doble de papel manteca. Reservar.</p>
<p>Colocar dentro de una cacerola el puré de manzana junto con la mitad del azúcar.</p>
<p>Llevar al fuego, calentar hasta que rompa el hervor y retirar. Adicionar la pectina mezclada con el azúcar restante, la glucosa, la esencia y colorante Fleibor. Unir bien.</p>
<p>Llevar nuevamente a fuego moderado, y continuar la cocción revolviendo constantemente con una cuchara de madera, hasta alcanzar una temperatura de 110°C. (Utilizar para ver testear un termómetro) Retirar del fuego.</p>
<p>Volcar la preparación dentro del recipiente preparado, y dejar solidificar a temperatura ambiente.</p>
<p>Retirar del molde y cortar los bombones cuadrados de 2 x 2, o emplear cortantes pequeños de diferentes formatos. Dejar orear por 24 horas en un lugar fresco y seco. Rebozar con azúcar molido y servir.</p>
						<br>
<p><strong>A Saber</strong></p>
<p>Para poder obtener bombones de otros gusto, seguir las indicaciones de la receta anterior, reemplazando la esencia de frutilla y el colorante Fleibor, por otros sabores.</p>
<p>Ejemplo: esencia de ananá y colorante en pasta amarillo, esencia de menta y colorante en pasta verde, esencia de naranja y colorante en pasta naranja, etc. 				</p>		
							
							
